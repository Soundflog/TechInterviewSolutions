# Колоду карт раздают по кругу, по одной карте каждому за раз, пока колода не кончится. Известен порядок карт в
# колоде. С кого должен начинать сдающий, чтобы первый игрок получил себе как можно больше тузов?
#
# Формат входных данных В первой строке вводятся два числа: количество игроков и количество карт в колоде (оба числа
# натуральные и не превосходят 100, количество карт делится на количество игроков).
#
# Во второй строке через пробел перечислены достоинства карт в том порядке, в котором они идут в колоде (6 –
# шестерка, 7 – семерка, 8 – восьмерка, 9 – девятка, 10 – десятка, 11 – валет, 12 – дама, 13 – король, 14 – туз). В
# колоде может быть произвольное число карт каждого достоинства.
#
# Формат выходных данных Выведите одно число – номер игрока, с которого следует начинать сдавать, чтобы первый игрок
# получил как можно больше тузов (игроки нумеруются числами 1, 2, 3, ...; сдача происходит по возрастанию номеров
# начиная с некоторого до последнего, и затем продолжается с первого). Если вариантов ответа несколько,
# выведите наименьший из них.

k = int(input())
m = int(input())
n = int(input())
# Рассчитаем количество циклов, необходимых для мытья всех машин
cycles = (n + k - 1) // k
# Общее время мытья всех машин
total_time = cycles * 2 * m
print(total_time)

