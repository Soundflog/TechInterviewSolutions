# 2 задание. На одной из встреч маркетологов решили провести розыгрыш, чтобы определить, кто будет отвечать за новую
# рекламную кампанию. Так как желающих и компетентных людей на встрече было много, было решено воспользоваться
# старой доброй считалочкой.
#
# Вам дан текст считалочки. Одно слово выпадает на одного человека. Если люди закончились раньше считалки,
# то обход людей начинается заново. Требуется определить, кто из присутствующих будет ответственным за новую
# рекламную кампанию.
#
# Формат входных данных В первой строке идет считалочка — последовательность слов из латинских букв, записанных
# через пробел. Количество слов в считалке больше 1, но не больше 100.
#
# Во второй строке в том же формате вводятся имена присутствующих маркетологов в порядке их пересчета.
# Гарантируется, что на встрече было более одного человека, но не более 100.
#
# Формат выходных данных
# Выведите имя выбранного маркетолога

# Чтение входных данных
counting_rhyme = input().strip().split()
marketers = input().strip().split()

# Количество слов в считалочке и количество маркетологов
num_words = len(counting_rhyme)
num_marketers = len(marketers)

# Определение индекса выбранного маркетолога
# Используем формулу для определения конечного индекса
selected_index = (num_words - 1) % num_marketers

# Вывод имени выбранного маркетолога
print(marketers[selected_index])
